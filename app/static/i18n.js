  function detect() {
    try {
      const saved = localStorage.getItem("mid_lang");
      if (saved && LANGS.includes(saved)) return saved;
      const n = (navigator.language || "en").slice(0, 2).toLowerCase();
      return LANGS.includes(n) ? n : "en";
    } catch { return "en"; }
  }

  let cur = detect();

  function t(key, vars) {
    let s = (D[cur] && D[cur][key]) || (D.en && D.en[key]) || key;
    if (vars) for (const k in vars) s = s.split("{" + k + "}").join(String(vars[k]));
    return s;
  }

  function apply(root) {
    root = root || document;
    root.querySelectorAll("[data-i18n]").forEach(el => { el.textContent = t(el.getAttribute("data-i18n")); });
    root.querySelectorAll("[data-i18n-html]").forEach(el => { el.innerHTML = t(el.getAttribute("data-i18n-html")); });
    root.querySelectorAll("[data-i18n-ph]").forEach(el => { el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph"))); });
    root.querySelectorAll("[data-i18n-title]").forEach(el => { el.setAttribute("title", t(el.getAttribute("data-i18n-title"))); });
    document.documentElement.lang = cur;
  }

  function setLang(l) {
    if (!LANGS.includes(l)) return;
    cur = l;
    try { localStorage.setItem("mid_lang", l); } catch {}
    apply();
    if (typeof window.onI18nChange === "function") window.onI18nChange();
  }

  window.I18N = { LANGS, NAMES, t, apply, setLang, get lang() { return cur; } };
